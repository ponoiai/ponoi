-- 33_file_size_limit.sql — поднимаем потолок размера файла в attachments/avatars (v1.150.0)
-- ВАЖНО: это меняет лимит на уровне бакета Storage, но не превращает загрузку в
-- resumable/chunked upload — uploadTo()/uploadWithProgress() в src/lib/storage.ts
-- по-прежнему шлют файл одним запросом, так что обрыв сети на 90% гигабайтного
-- файла означает начать заново. Для настоящей надёжности на очень больших файлах
-- в будущем стоит перейти на TUS-протокол (supabase-js это поддерживает) — это
-- отдельная задача, не в этой миграции.
-- Также: на бесплатном/части платных планов Supabase сам сервис может enforced-ить
-- свой собственный потолок размера файла поверх этой настройки бакета (это
-- настройка проекта в Dashboard -> Settings -> Storage, не SQL) — если после этой
-- миграции загрузка больших файлов всё ещё режется, проверь этот лимит там.
update storage.buckets set file_size_limit = 42949672960 where id in ('attachments', 'avatars');   -- 40 ГБ = 40 * 1024^3
