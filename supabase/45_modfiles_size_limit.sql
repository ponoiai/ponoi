-- 45_modfiles_size_limit.sql — тот же потолок, что у attachments/avatars (см.
-- 33_file_size_limit.sql), но для bucket modfiles (QuickLaunch) — при создании
-- бакета (44_modfiles_storage.sql) file_size_limit не задавался вообще, значит
-- бакет действовал по глобальному лимиту проекта без собственного оверрайда.
-- Моды (.jar) бывают под сотню МБ каждый — 40 ГБ с запасом хватает на любой мод.
--
-- ВАЖНО: как и с attachments/avatars, это лимит НА УРОВНЕ БАКЕТА. Поверх него
-- есть ЕЩЁ глобальный лимит размера файла всего проекта (Dashboard -> Settings ->
-- Storage -> "Max file size" или похожее) — он режет ВСЕ бакеты сразу и SQL'ем
-- не меняется. Судя по симптому «файл больше 10 МБ — ошибка», упирается именно
-- в него — эта миграция одна проблему не решит, нужно ещё поднять лимит там.
update storage.buckets set file_size_limit = 42949672960 where id = 'modfiles';   -- 40 ГБ = 40 * 1024^3
